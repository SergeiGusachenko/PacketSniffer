void print_payload(u_char *data, int len)
{

	 for(int i=0 ; i < len ; i++)
    {
        if( i!=0 && i%16==0)   //if one line of hex printing is complete...
        {
            printf("         ");
            for(int j=i-16 ; j<i ; j++)
            {
                if(data[j]>=32 && data[j]<=128)
                    printf("%c",(unsigned char)data[j]); //if its a number or alphabet
                else printf("."); //otherwise print a dot
            }
            printf("\n");
        }

        if(i%16==0) printf("   ");
            printf(" %02X",(unsigned int)data[i]);

        if( i==len -1)  //print the last spaces
        {
            for(int j=0;j<15-i%16;j++) printf("   "); //extra spaces

        printf("         ");

            for(int j=i-i%16 ; j<=i ; j++)
            {
                if(data[j]>=32 && data[j]<=128) printf("%c",(unsigned char)data[j]);
                else printf(".");
            }
            printf("\n");
        }
    }
return;
}



void print_udp_packet(u_char *Buffer , int Size)
{

    unsigned short iphdrlen;

    struct iphdr *iph = (struct iphdr *)(Buffer +  sizeof(struct ethhdr));
    iphdrlen = iph->ihl*4;

    struct udphdr *udph = (struct udphdr*)(Buffer + iphdrlen  + sizeof(struct ethhdr));

    int header_size =  sizeof(struct ethhdr) + iphdrlen + sizeof udph;

	printf( "\n\n***********************UDP Packet*************************\n");
    print_payload(Buffer+iphdrlen , sizeof udph);
    printf( "Data Payload\n");
    //Move the pointer ahead and reduce the size of string
    print_payload(Buffer + header_size , Size - header_size);
    printf( "\n###########################################################");
}


